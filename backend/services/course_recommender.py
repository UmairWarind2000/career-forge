def get_courses_for_skill(skill: str, max_courses: int = 3) -> list:
    skill_lower = skill.lower()
    courses = COURSE_DATABASE.get(skill_lower, None)

    if not courses:
        default = COURSE_DATABASE["default"]
        courses = [
            {
                **course,
                "title": f"Learn {skill.title()} - {course['title']}",
                "url": course["url"] + skill.replace(" ", "+")
            }
            for course in default
        ]

    return courses[:max_courses]

def recommend_courses_for_roadmap(roadmap_items: list) -> list:
    recommendations = []
    for item in roadmap_items:
        skill = item.get("skill", "")
        courses = get_courses_for_skill(skill)
        recommendations.append({
            "skill": skill,
            "step": item.get("step", 0),
            "priority": item.get("priority", "medium"),
            "difficulty_label": item.get("difficulty_label", "Intermediate"),
            "estimated_weeks": item.get("estimated_weeks", 2),
            "courses": courses
        })
    return recommendations